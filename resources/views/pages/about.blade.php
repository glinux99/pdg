                        <p class="col-md-10 mx-auto">
                            Le parti est d'abord connu sous le nom de Bloc démocratique gabonais sous la direction de Léon Mba. Avec l'accession de Mba à la présidence, le parti devient dominant. Devenu président du Gabon en 1967, Albert Bernard Bongo, renomme le parti en Parti démocratique gabonais, le 12 mars 1968. Le président Bongo rend illégaux tous les autres partis, faisant du PDG le parti unique du Gabon.

                            Au cours des deux décennies du monopartisme, le PDG prend de l'essor et conforte sa position sur l'échiquier politique national, ce qui explique son hégémonie. Il est le seul parti avec une présence réelle dans toutes les provinces du Gabon.

                            La fin du parti unique en 1991 ne change rien à la mainmise du PDG sur la politique gabonaise. L'appareil d'État est quasi entièrement composé de fonctionnaires membres du PDG et la presse, qu'elle soit étatique ou privée, reste dans la plupart des cas le porte-voix du PDG et surtout du président Bongo.

                            Lors des élections législatives du 9 décembre 2001, le PDG remporte 88 des 120 sièges de l'Assemblée nationale gabonaise.

                            Aujourd'hui, une constellation de partis politiques s'est formée autour du PDG pour constituer le bloc de la majorité présidentielle du Gabon.

                            Aux élections législatives du 17 décembre 2006, le PDG a remporté 80 sièges sur 1201.
                        </p>
                        <p class="col-md-10 mx-auto">


                            Après de graves dissensions internes2, le PDG a nommé Ali Bongo comme candidat à l'élection présidentielle anticipée de 20093, qui fait suite à la mort d'Omar Bongo. Ali Bongo, ministre de la Défense et fils du président défunt est élu le 30 septembre 20094.
                            12 mars 1968 : Albert-Bernard Bongo créé le Parti Démocratique Gabonais (PDG)



                            Le lendemain à peine de cette création, c'est à dire le 13 mars 1968, le PDG est institué comme parti unique de la République Gabonaise

                            Le Multipartisme est dissout, entrainant ainsi la dissolution immédiate et automatique de tous les autres partis politiques !
                        </p>
                        <p class="col-md-10 mx-auto">
                            Il n y a rien de spectaculaire à cela. En effet, l'institution d'un parti unique, fut chose courante après les indépendances des anciennes colonies françaises, pour entre autres, selon les dirigeants de l'époque, développer des sentiments nationalistes, et raffermir ainsi l'unité nationale.



                            4 ans plus tard, Albert Bernard Bongo devenu Omar Bongo, constitutionnalise le PDG à travers l'article 4 de la Constitution :

                            « Le Parti démocratique gabonais assure la participation des électeurs à tous suffrages. Il est garant de l’unité nationale et œuvre pour la promotion économique et sociale du pays. Il établit la liaison nécessaire entre le gouvernement et le peuple gabonais.

                            Nul ne peut se voir confier un mandat public électif, s’il n’est investi par le Parti ».



                            52 ans après sa création, quel bilan dresse t-on de l'apport de ce parti dans le développement et la construction de notre pays ?
                        </p>
